/**
 * Get emails to send AFTER the first list
 *
 * This function:
 * 1. Fetches all users from the database
 * 2. Filters out emails that exist in firstEmailList
 * 3. Filters for only active diamond_lead or dollar_lead subscriptions
 * 4. Returns only emails NOT in firstEmailList with valid subscriptions
 *
 * Use this for second round of emails to users who weren't in the first batch
 */
export async function getEmailsAfterFirstList(): Promise<string[]> {
  try {
    /**
     * IMPORTANT: Why we create Supabase client directly here
     *
     * We cannot use userRepository or import from @/utils/supabase/server because:
     * 1. Those files use Next.js cookies() function from 'next/headers'
     * 2. cookies() only works in Server Components, Route Handlers, and Server Actions
     * 3. Cron jobs run in a different context where cookies() is not available
     * 4. Importing those files causes "Ecmascript file had an error" at build/import time
     *
     * Solution:
     * - Create Supabase admin client directly using service role key
     * - Use dynamic import to load @supabase/supabase-js only when function is called
     * - This bypasses Row Level Security (RLS) and doesn't need user authentication
     * - Works perfectly in cron jobs, webhooks, and other server-side contexts
     */
    const { createClient } = await import('@supabase/supabase-js');

    const supabase = createClient(
      process.env.NEXT_PUBLIC_SUPABASE_URL!,
      process.env.SUPABASE_SERVICE_ROLE_KEY!,
      {
        auth: {
          autoRefreshToken: false,
          persistSession: false
        }
      }
    );

    // Get all users from database with subscription info
    const { data: allUsers, error } = await supabase
      .from('users')
      .select('email, subscription_plan, subscription_status')
      .order('created_at', { ascending: false });

    if (error || !allUsers) {
      console.error('❌ Failed to fetch users:', error);
      return [];
    }

    // Create a Set of all emails in firstEmailList for fast lookup
    const firstEmailSet = new Set<string>();
    firstEmailList.Diamond.forEach(email => firstEmailSet.add(email.toLowerCase()));
    firstEmailList.Dollar.forEach(email => firstEmailSet.add(email.toLowerCase()));

    // Filter users: only keep emails NOT in firstEmailList AND have active diamond/dollar subscriptions
    const filteredEmails = allUsers
      .filter(user => {
        // Must have email
        if (!user.email || user.email.trim() === '') return false;

        // Must NOT be in firstEmailList
        if (firstEmailSet.has(user.email.toLowerCase())) return false;

        // Must have active subscription
        if (user.subscription_status !== 'active') return false;

        // Must be diamond_lead or dollar_lead
        if (user.subscription_plan !== 'diamond-lead' && user.subscription_plan !== 'dollar-lead') return false;

        return true;
      })
      .map(user => user.email);

    console.log(`📊 Email filtering stats:`);
    console.log(`   Total users in DB: ${allUsers.length}`);
    console.log(`   Emails in firstEmailList: ${firstEmailSet.size}`);
    console.log(`   Emails to send (after first list + active subscription): ${filteredEmails.length}`);

    return filteredEmails;
  } catch (error) {
    console.error('❌ Failed to get emails after first list:', error);
    return [];
  }
}